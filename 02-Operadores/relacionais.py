valor1 = 1
valor2 = 2

igual = valor1 == valor2
maior = valor1 > valor2
menor = valor1 < valor2
maior_igual = valor1 >= valor2
menor_igual = valor1 <= valor2
diferente = valor1 != valor2

print("Igual: ",igual)
print("Maior: ",maior)
print("Menor: ",menor)
print("Maior ou Igual: ",maior_igual)
print("Menor ou Igual: ",menor_igual)
print("Diferente: ",diferente)